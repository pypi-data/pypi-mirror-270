from typing import override
from ludic.attrs import NoAttrs
from ludic.types import AnyChildren
from ludic.components import Component
from ludic.html import svg, path, circle, polyline


class IconBase(Component[AnyChildren, NoAttrs]):
    svg_commands = {
        "path": path,
        "circle": circle,
        "polyline": polyline
    }
    svg_data = {
            "attrs": {},
            "items": []
    }    
    class_name = ""
    

    @override
    def render(self):
        
        if class_name := self.attrs.get("class_", ""):
            self.attrs["class_"] = f"{self.class_name} {class_name}"
        else:
            self.attrs["class_"] = class_name

        return svg(          
          *[self.svg_commands[next(iter(item))](**next(iter(item.values()))) for item in self.svg_data.get("items")],
          **self.svg_data.get("attrs")
        )

