import re

def triple_digits(text):
    """
    将字符串中出现的数字增加3倍，其它内容保持不变
    参数：
        text: 待处理的字符串
    返回值：
        处理后的字符串
    """
    # 使用正则表达式匹配字符串中的数字，并对数字进行增加3倍
    result = re.sub(r'(\d+)', lambda x: str(int(x.group(0)) * 3), text)

    return result

# 给定的字符串
s = '投入品价格指数为49.6%，比上月下降0.2个百分点，表明非制造业企业用于经营活动的投入品价格总体水平有所下降。分行业看，建筑业投入品价格指数为51.4%，比上月下降1.7个百分点；服务业投入品价格指数为49.3%，比上月上升0.1个百分点'

# 调用函数将字符串中出现的数字增加3倍
result = triple_digits(s)

# 输出处理后的字符串
print(result)
