def add_number(a,b):
    """
    Args:
        a (int): Primer numero a sumar
        b (int): Segundo numero a sumar
    
    Ejemplo:
    >>> add_numbers(2,3)
    5
    """
    return a + b

def substract_number(a,b):
    """
    Args:
        a (int): Primer numero a restar
        b (int): Segundo numero a restar
    
    Ejemplo:
    >>> substract_number(4,2)
    2
    """
    return a - b

def multiply_number(a,b):
    """
    Args:
        a (int): Primer numero a multiplicar
        b (int): Segundo numero a multiplicar
    
    Ejemplo:
    >>> multiply_number(4,2)
    8
    """
    return a * b

